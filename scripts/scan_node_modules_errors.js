const fs = require('fs');
const path = require('path');
const { keccak256, stringToBytes } = require('viem');

const targets = ['0x40d8d67b', '0x18dce79f'];

console.log('Scanning node_modules for error selectors:', targets);

function scanDir(dir) {
  const files = fs.readdirSync(dir);
  for (const file of files) {
    const fullPath = path.join(dir, file);
    const stat = fs.statSync(fullPath);
    if (stat.isDirectory()) {
      if (file !== '.bin' && file !== 'typescript') {
        scanDir(fullPath);
      }
    } else if (file.endsWith('.json') || file.endsWith('.js')) {
      try {
        const content = fs.readFileSync(fullPath, 'utf8');
        if (content.includes('error') && (content.includes('40d8d67b') || content.includes('18dce79f') || content.includes('executeDirectMinting'))) {
          console.log(`Potential Match File: ${fullPath}`);
        }
      } catch (err) {
        // ignore
      }
    }
  }
}

scanDir(path.join(__dirname, '../node_modules'));
